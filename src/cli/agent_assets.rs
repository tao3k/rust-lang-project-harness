#[derive(Debug, Default, Clone, PartialEq, Eq)]
pub(super) enum AgentConfigScope {
    #[default]
    Project,
    CodexProfile(String),
}

impl AgentConfigScope {
    pub(super) fn codex_profile(profile: Option<String>) -> Result<Self, String> {
        let profile = profile.unwrap_or_else(|| DEFAULT_CODEX_PROFILE.to_string());
        validate_codex_profile_name(&profile)?;
        Ok(Self::CodexProfile(profile))
    }

    fn label(&self) -> &'static str {
        match self {
            Self::Project => "project",
            Self::CodexProfile(_) => "profile",
        }
    }

    pub(super) fn profile_name(&self) -> Option<&str> {
        match self {
            Self::Project => None,
            Self::CodexProfile(profile) => Some(profile),
        }
    }
}

pub(super) fn default_codex_profile_name() -> &'static str {
    DEFAULT_CODEX_PROFILE
}

pub(super) fn install_agent_assets(
    project_root: &Path,
    client: &str,
    scope: &AgentConfigScope,
) -> Result<(), String> {
    if client != "codex" {
        return Err(format!("unsupported agent client: {client}"));
    }
    let skill_dir = project_root.join(CODEX_SKILL_DIR);
    let policy_path = project_root.join(CODEX_POLICY_PATH);
    fs::create_dir_all(project_root.join(CODEX_DIR))
        .map_err(|error| format!("failed to create codex dir: {error}"))?;
    fs::create_dir_all(&skill_dir)
        .map_err(|error| format!("failed to create agent skill dir: {error}"))?;
    fs::write(skill_dir.join("SKILL.org"), AGENT_SKILL_CONTENT)
        .map_err(|error| format!("failed to write agent skill: {error}"))?;
    fs::write(policy_path, AGENT_POLICY_CONTENT)
        .map_err(|error| format!("failed to write agent hook policy: {error}"))?;
    let config_path = codex_config_path(project_root, scope)?;
    if let Some(parent) = config_path.parent() {
        fs::create_dir_all(parent)
            .map_err(|error| format!("failed to create Codex config dir: {error}"))?;
    }
    let existing_config = if config_path.exists() {
        fs::read_to_string(&config_path)
            .map_err(|error| format!("failed to read codex config.toml: {error}"))?
    } else {
        String::new()
    };
    fs::write(config_path, merge_agent_config_toml(&existing_config))
        .map_err(|error| format!("failed to write codex config.toml: {error}"))?;
    Ok(())
}

fn agent_config_toml() -> String {
    format!(
        r#"# Generated by `rs-harness agent install --client codex`.
#
unified_exec = true

{}
"#,
        rs_harness_agent_hooks_block()
    )
}

fn merge_agent_config_toml(existing: &str) -> String {
    let trimmed = existing.trim();
    if trimmed.is_empty() {
        return agent_config_toml();
    }

    let without_orphans = drop_orphan_hook_subsections(existing);
    let without_legacy = strip_legacy_rs_harness_hooks(&without_orphans);
    let with_unified_exec = ensure_unified_exec(&without_legacy);
    replace_or_append_managed_block(&with_unified_exec, &rs_harness_agent_hooks_block())
}

