fn ensure_unified_exec(config: &str) -> String {
    if config_has_unified_exec(config) {
        return ensure_trailing_newline(config.trim_end().to_string());
    }
    format!("unified_exec = true\n\n{}", config.trim_start())
}

fn replace_or_append_managed_block(config: &str, block: &str) -> String {
    let block = block.trim_end();
    let Some(start) = config.find(RS_HARNESS_BLOCK_BEGIN) else {
        return ensure_trailing_newline(format!("{}\n\n{block}", config.trim_end()));
    };
    let Some(end_offset) = config[start..].find(RS_HARNESS_BLOCK_END) else {
        return ensure_trailing_newline(format!("{}\n\n{block}", config.trim_end()));
    };
    let end = start + end_offset + RS_HARNESS_BLOCK_END.len();
    let rest_start = if config[end..].starts_with('\n') {
        end + 1
    } else {
        end
    };
    let before = config[..start].trim_end();
    let before = if is_legacy_rs_harness_preamble(before) {
        "unified_exec = true"
    } else {
        before
    };
    let after = config[rest_start..].trim_start_matches('\n').trim_end();
    let mut merged = String::new();
    if !before.is_empty() {
        merged.push_str(before);
        merged.push_str("\n\n");
    }
    merged.push_str(block);
    if !after.is_empty() {
        merged.push_str("\n\n");
        merged.push_str(after);
    }
    ensure_trailing_newline(merged)
}

fn strip_legacy_rs_harness_hooks(config: &str) -> String {
    if config.contains(RS_HARNESS_BLOCK_BEGIN)
        || !config.contains("rs-harness agent hook --client codex")
    {
        return ensure_trailing_newline(config.trim_end().to_string());
    }

    let sections = split_toml_hook_sections(config);
    let mut kept = Vec::new();
    for (index, section) in sections.iter().enumerate() {
        if index == 0 && is_legacy_rs_harness_preamble(&section.text) {
            continue;
        }
        if section
            .text
            .contains("rs-harness agent hook --client codex")
        {
            continue;
        }
        if let Some(event) = section.event_name()
            && sections.get(index + 1).is_some_and(|next| {
                next.hook_event_name().as_deref() == Some(event.as_str())
                    && next.text.contains("rs-harness agent hook --client codex")
            })
        {
            continue;
        }
        kept.push(section.text.trim_end());
    }
    drop_orphan_hook_subsections(&ensure_trailing_newline(
        kept.join("\n\n").trim().to_string(),
    ))
}

fn is_legacy_rs_harness_preamble(section: &str) -> bool {
    section.contains("# Generated by `rs-harness agent install --client codex`")
        && section.contains("Rust source reads are routed through rs-harness search packets")
}

struct TomlHookSection {
    text: String,
}

impl TomlHookSection {
    fn event_name(&self) -> Option<String> {
        parse_hook_header(self.header()?, false)
    }

    fn hook_event_name(&self) -> Option<String> {
        parse_hook_header(self.header()?, true)
    }

    fn header(&self) -> Option<&str> {
        self.text
            .lines()
            .find(|line| line.trim_start().starts_with("[[hooks."))
            .map(str::trim)
    }
}

fn split_toml_hook_sections(config: &str) -> Vec<TomlHookSection> {
    config
        .lines()
        .fold(TomlHookSectionBuilder::default(), |builder, line| {
            builder.push_line(line)
        })
        .finish()
}

#[derive(Default)]
struct TomlHookSectionBuilder {
    sections: Vec<TomlHookSection>,
    current: Vec<String>,
}

impl TomlHookSectionBuilder {
    fn push_line(mut self, line: &str) -> Self {
        if line.trim_start().starts_with("[[hooks.") && !self.current.is_empty() {
            self.sections.push(TomlHookSection {
                text: self.current.join("\n"),
            });
            self.current.clear();
        }
        self.current.push(line.to_string());
        self
    }

    fn finish(mut self) -> Vec<TomlHookSection> {
        if !self.current.is_empty() {
            self.sections.push(TomlHookSection {
                text: self.current.join("\n"),
            });
        }
        self.sections
    }
}

fn drop_orphan_hook_subsections(config: &str) -> String {
    let sections = split_toml_hook_sections(config);
    let kept = sections
        .into_iter()
        .fold(HookParentFilter::default(), |filter, section| {
            filter.push_section(section)
        })
        .finish();
    ensure_trailing_newline(kept.join("\n\n").trim().to_string())
}

#[derive(Default)]
struct HookParentFilter {
    seen_events: BTreeSet<String>,
    kept: Vec<String>,
}

impl HookParentFilter {
    fn push_section(mut self, section: TomlHookSection) -> Self {
        if let Some(event) = section.event_name() {
            self.seen_events.insert(event);
            self.kept.push(section.text.trim_end().to_string());
            return self;
        }
        if section
            .hook_event_name()
            .is_none_or(|event| self.seen_events.contains(&event))
        {
            self.kept.push(section.text.trim_end().to_string());
        }
        self
    }

    fn finish(self) -> Vec<String> {
        self.kept
    }
}

fn parse_hook_header(header: &str, nested_hook: bool) -> Option<String> {
    let event = header.strip_prefix("[[hooks.")?.strip_suffix("]]")?;
    if nested_hook {
        event.strip_suffix(".hooks").map(str::to_owned)
    } else if event.ends_with(".hooks") {
        None
    } else {
        Some(event.to_owned())
    }
}

fn ensure_trailing_newline(mut text: String) -> String {
    text.push('\n');
    text
}

pub(super) fn print_agent_doctor(
    project_root: &Path,
    action: &str,
    client: Option<&str>,
    scope: &AgentConfigScope,
) -> Result<(), String> {
    let Some(client) = client else {
        println!(
            "[agent-doctor] action={action} client=none skill=false policy=false config=false hooks=0 daemon=missing protocol=cli-cold"
        );
        println!(
            "|note kind=client-required message=\"pass --client codex to inspect Codex assets\""
        );
        return Ok(());
    };
    if client != "codex" {
        return Err(format!("unsupported agent client: {client}"));
    }
    let skill = project_root.join(CODEX_SKILL_DIR).join("SKILL.org");
    let policy = project_root.join(CODEX_POLICY_PATH);
    let config = codex_config_path(project_root, scope)?;
    let config_exists = config.exists();
    let config_text = if config_exists {
        fs::read_to_string(&config).unwrap_or_default()
    } else {
        String::new()
    };
    let unified_exec_present = config_has_unified_exec(&config_text);
    let namespaced_matcher_present = config_has_active_pretool_matcher(&config_text);
    let hooks = if config_exists {
        CODEX_CONFIG_HOOK_COUNT
    } else {
        0
    };
    if let Some(profile) = scope.profile_name() {
        println!(
            "[agent-doctor] action={action} client=codex scope={} profile={profile} skill={} policy={} config={} hooks={hooks} daemon=missing protocol=cli-cold",
            scope.label(),
            skill.exists(),
            policy.exists(),
            config_exists,
        );
    } else {
        println!(
            "[agent-doctor] action={action} client=codex skill={} policy={} config={} hooks={hooks} daemon=missing protocol=cli-cold",
            skill.exists(),
            policy.exists(),
            config_exists,
        );
    }
    println!(
        "|skill {} present={}",
        display_cli_path(project_root, &skill),
        skill.exists()
    );
    println!(
        "|policy {} present={}",
        display_cli_path(project_root, &policy),
        policy.exists()
    );
    println!(
        "|config {} present={}",
        display_config_path(project_root, &config, scope),
        config_exists
    );
    if let Some(profile) = scope.profile_name() {
        println!("|profile name={profile} usage=\"codex --profile {profile} exec -C <repo> ...\"");
        println!("|compat codex_exec_hooks runtime=requires-smoke-test enforced=unknown");
        println!(
            "|warn kind=codex-exec-hooks message=\"Codex CLI exec hook execution is runtime-dependent; verify with a smoke test before relying on profile enforcement\""
        );
    }
    if config_exists {
        println!("|compat unified_exec present={unified_exec_present} required=true");
        println!("|compat matcher namespaced={namespaced_matcher_present} required=true");
    }
    if config_exists && !unified_exec_present {
        println!(
            "|warn kind=codex-unified-exec message=\".codex/config.toml should set unified_exec = true for Desktop exec hooks\""
        );
    }
    if config_exists && !namespaced_matcher_present {
        println!(
            "|warn kind=codex-tool-matcher message=\"hook matcher should cover namespaced tools such as functions.exec_command\""
        );
    }
    for event in CODEX_CONFIG_HOOK_EVENTS {
        println!("|hook event={event} source=config present={config_exists}");
    }
    Ok(())
}

fn codex_config_path(project_root: &Path, scope: &AgentConfigScope) -> Result<PathBuf, String> {
    match scope {
        AgentConfigScope::Project => Ok(project_root.join(CODEX_CONFIG)),
        AgentConfigScope::CodexProfile(profile) => {
            Ok(codex_home()?.join(format!("{profile}.config.toml")))
        }
    }
}

fn codex_home() -> Result<PathBuf, String> {
    if let Some(home) = env::var_os("CODEX_HOME") {
        return Ok(PathBuf::from(home));
    }
    env::var_os("HOME")
        .map(|home| PathBuf::from(home).join(".codex"))
        .ok_or_else(|| "expected CODEX_HOME or HOME for Codex profile install".to_string())
}

fn validate_codex_profile_name(profile: &str) -> Result<(), String> {
    if !profile.is_empty()
        && profile
            .bytes()
            .all(|byte| byte.is_ascii_alphanumeric() || byte == b'-' || byte == b'_')
    {
        return Ok(());
    }
    Err(format!(
        "invalid Codex profile name: {profile}; expected ASCII letters, digits, '-' or '_'"
    ))
}

fn config_has_unified_exec(config: &str) -> bool {
    config
        .lines()
        .any(|line| line.trim() == "unified_exec = true")
}

fn config_has_active_pretool_matcher(config: &str) -> bool {
    let mut in_pre_tool = false;
    for line in config.lines().map(str::trim) {
        if line == "[[hooks.PreToolUse]]" {
            in_pre_tool = true;
            continue;
        }
        if line.starts_with("[[hooks.") {
            in_pre_tool = false;
        }
        if in_pre_tool && let Some(matcher) = line.strip_prefix("matcher = ") {
            return matcher.contains(".*(")
                && matcher.contains("exec_command")
                && matcher.contains("command_execution")
                && matcher.contains("fs\\\\.read")
                && matcher.contains("Read")
                && matcher.contains("Write")
                && !matcher.contains("disabled");
        }
    }
    false
}

fn display_cli_path(root: &Path, path: &Path) -> String {
    path.strip_prefix(root)
        .unwrap_or(path)
        .display()
        .to_string()
        .replace('\\', "/")
}

fn display_config_path(root: &Path, path: &Path, scope: &AgentConfigScope) -> String {
    if matches!(scope, AgentConfigScope::Project) {
        return display_cli_path(root, path);
    }
    path.display().to_string().replace('\\', "/")
}
